package com.example.menpa.billingapp;

public class cartbean {

    int cartbillid;
    String cartitemid;
    String cartitemname;
    String cartitemweight;
    String cartitemprice;
    String cartitemquantity;


    public int getCartbillid() {
        return cartbillid;
    }

    public void setCartbillid(int cartbillid) {
        this.cartbillid = cartbillid;
    }

    public String getCartitemid() {
        return cartitemid;
    }

    public void setCartitemid(String cartitemid) {
        this.cartitemid = cartitemid;
    }

    public String getCartitemname() {
        return cartitemname;
    }

    public void setCartitemname(String cartitemname) {
        this.cartitemname = cartitemname;
    }

    public String getCartitemweight() {
        return cartitemweight;
    }

    public void setCartitemweight(String cartitemweight) {
        this.cartitemweight = cartitemweight;
    }

    public String getCartitemprice() {
        return cartitemprice;
    }

    public void setCartitemprice(String cartitemprice) {
        this.cartitemprice = cartitemprice;
    }

    public String getCartitemquantity() {
        return cartitemquantity;
    }

    public void setCartitemquantity(String cartitemquantity) {
        this.cartitemquantity = cartitemquantity;
    }
}
